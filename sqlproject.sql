CREATE DATABASE FoodOrderDB;
use FoodOrderDB;


-- ===========================
-- 1. Customers
-- ===========================

CREATE TABLE Customers (
    customer_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_name VARCHAR(100) NOT NULL,
    phone VARCHAR(15) UNIQUE,
    email VARCHAR(100) UNIQUE,
    address VARCHAR(255)
);

-- ===========================
-- 2. Restaurants
-- ===========================

CREATE TABLE Restaurants (
    restaurant_id INT AUTO_INCREMENT PRIMARY KEY,
    restaurant_name VARCHAR(100) NOT NULL,
    location VARCHAR(100),
    phone VARCHAR(15),
    rating DECIMAL(2,1) CHECK (rating BETWEEN 1.0 AND 5.0)
);

-- ===========================
-- 3. Categories
-- ===========================

CREATE TABLE Categories (
    category_id INT AUTO_INCREMENT PRIMARY KEY,
    category_name VARCHAR(50) NOT NULL UNIQUE
);

-- ===========================
-- 4. Menu
-- ===========================

CREATE TABLE Menu (
    menu_id INT AUTO_INCREMENT PRIMARY KEY,
    restaurant_id INT NOT NULL,
    category_id INT NOT NULL,
    food_name VARCHAR(100) NOT NULL,
    price DECIMAL(10,2) NOT NULL,
    availability ENUM('Available','Unavailable') DEFAULT 'Available',

    CONSTRAINT fk_menu_restaurant
        FOREIGN KEY (restaurant_id)
        REFERENCES Restaurants(restaurant_id),

    CONSTRAINT fk_menu_category
        FOREIGN KEY (category_id)
        REFERENCES Categories(category_id)
);

-- ===========================
-- 5. Orders
-- ===========================

CREATE TABLE Orders (
    order_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT NOT NULL,
    order_date DATETIME DEFAULT CURRENT_TIMESTAMP,
    total_amount DECIMAL(10,2),
    status ENUM('Pending','Preparing','Out for Delivery','Delivered','Cancelled')
           DEFAULT 'Pending',

    CONSTRAINT fk_order_customer
        FOREIGN KEY (customer_id)
        REFERENCES Customers(customer_id)
);

-- ===========================
-- 6. Order_Items
-- ===========================

CREATE TABLE Order_Items (
    order_item_id INT AUTO_INCREMENT PRIMARY KEY,
    order_id INT NOT NULL,
    menu_id INT NOT NULL,
    quantity INT NOT NULL,
    subtotal DECIMAL(10,2),

    CONSTRAINT fk_orderitem_order
        FOREIGN KEY (order_id)
        REFERENCES Orders(order_id),

    CONSTRAINT fk_orderitem_menu
        FOREIGN KEY (menu_id)
        REFERENCES Menu(menu_id)
);

-- ===========================
-- 7. Payments
-- ===========================

CREATE TABLE Payments (
    payment_id INT AUTO_INCREMENT PRIMARY KEY,
    order_id INT NOT NULL,
    payment_method ENUM('Cash','UPI','Credit Card','Debit Card','Net Banking'),
    payment_status ENUM('Paid','Pending','Failed') DEFAULT 'Paid',
    payment_date DATETIME DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_payment_order
        FOREIGN KEY (order_id)
        REFERENCES Orders(order_id)
);

-- ===========================
-- 8. Delivery_Partners
-- ===========================

CREATE TABLE Delivery_Partners (
    partner_id INT AUTO_INCREMENT PRIMARY KEY,
    partner_name VARCHAR(100),
    phone VARCHAR(15),
    vehicle_number VARCHAR(20),
    rating DECIMAL(2,1) CHECK (rating BETWEEN 1.0 AND 5.0)
);

-- ===========================
-- 9. Deliveries
-- ===========================

CREATE TABLE Deliveries (
    delivery_id INT AUTO_INCREMENT PRIMARY KEY,
    order_id INT NOT NULL,
    partner_id INT NOT NULL,
    delivery_status ENUM('Assigned','Picked Up','On the Way','Delivered')
                    DEFAULT 'Assigned',
    delivery_time DATETIME,

    CONSTRAINT fk_delivery_order
        FOREIGN KEY (order_id)
        REFERENCES Orders(order_id),

    CONSTRAINT fk_delivery_partner
        FOREIGN KEY (partner_id)
        REFERENCES Delivery_Partners(partner_id)
);

-- ===========================
-- 10. Reviews
-- ===========================

CREATE TABLE Reviews (
    review_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT NOT NULL,
    restaurant_id INT NOT NULL,
    rating INT CHECK (rating BETWEEN 1 AND 5),
    review_text VARCHAR(500),
    review_date DATE,

    CONSTRAINT fk_review_customer
        FOREIGN KEY (customer_id)
        REFERENCES Customers(customer_id),

    CONSTRAINT fk_review_restaurant
        FOREIGN KEY (restaurant_id)
        REFERENCES Restaurants(restaurant_id)
);

show tables;
DESC Customers;
DESC Restaurants;
DESC Categories;
DESC Menu;
DESC Orders;
DESC Order_Items;
DESC Payments;
DESC Delivery_Partners;
DESC Deliveries;
DESC Reviews;


